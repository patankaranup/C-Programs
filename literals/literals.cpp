#include <iostream>
using namespace std;
int main()
{
    int n, m, o, b; // integer literals
    n = 25;         // decimal number
    m = 031;        // octal number represented by writing 0 at first
    o = 0x19;       // hexadecimal number represented by writing 0x at first
    b = 0b11001;    // binary number represented with 0b at first
    cout << n << endl;
    cout << m << endl;
    cout << o << endl;
    cout << b << endl;

    return 0;
}