#include <iostream>
using namespace std;
int main()
{
    float n, m, o, p;
    n = 3.14159;    // normal number
    m = 3.14159E2;  // number multiply by 10 to the exponent of 2
    o = 3.14159e2;  // same as above
    p = 3.14159E-1; // number multiply by 10 to the exponent of -1
    cout << n << endl;
    cout << m << endl;
    cout << o << endl;
    cout << p << endl;

    return 0;
}